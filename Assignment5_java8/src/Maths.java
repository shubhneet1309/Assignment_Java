public class Maths {


    public Integer add(Integer x, Integer y) {
      //  System.out.println(x + y);
        return x + y;
    }
    public Integer sub(Integer x,Integer y)
    {
     //   System.out.println(x-y);
        return x-y;
    }
    public static Integer prod(Integer x, Integer y){
        return x*y;
    }
}
